from setuptools import find_packages, setup, Command

# Package meta-data.
NAME = 'scitoolkit'
DESCRIPTION = 'Collection of useful scientific functionalities.'
URL = 'https://github.com/craymichael/scitoolkit'
EMAIL = 'zjc2920@rit.edu'
AUTHOR = 'Zach Carmichael'
# Python2 2.7.0+ or Python 3 3.5.0+
REQUIRES_PYTHON = '>=2.7,!=3.0*,!=3.1*,!=3.2*,!=3.3*,!=3.4*'
VERSION = None

# TODO add versions
